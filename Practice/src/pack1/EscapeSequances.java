package pack1;

public class EscapeSequances {

	public static void main(String[] args) {
		
		
		
		//how do escape sequence by using backslash 
		System.out.println("this is \"my house\"");
		System.out.println("this is \\my house\\");
		System.out.println("this is \n my house");
		System.out.println("this is \t my house");
	}

}
